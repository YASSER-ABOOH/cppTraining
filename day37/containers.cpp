/*
* CONTAINERS *
==============
***Sequence Containers***

-stores elements in a linear sequence.
-order of elements is maintained as per the sequence in which they were inserted.
-ex: 
-- vector, dequeue, array, list, forward-list, string
==============
***Associative Containers***

-stores elements in a way that allows fast retrival based in keys.
-elements are ordered by keys.
-ex:
-- set, multiset, map, multimap
==============
***Unorderd Containers***

-similar to assoc. cont. but do not maintain any specific order.
-provides average constant time complexity for search, insert, and delete operations.
-ex:
-- unordered_set, unordered_multidet, unordered_map, unordered_multimap


*/